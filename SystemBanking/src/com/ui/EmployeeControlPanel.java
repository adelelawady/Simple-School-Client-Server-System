    public void StartTimer(){
        int delay = 1; //milliseconds
        ActionListener taskPerformer = new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                try {
                    
                    //...Perform a task...
                    if(jTextField1.getText().isEmpty()){
                        jLabel12.setVisible(false);
                    }
                    if(jTextField2.getText().isEmpty()){
                        jLabel9.setVisible(false);
                        jLabel10.setVisible(false);
                    }
                    if (jTextField6.getText().equals(UI.password)){
                        jLabel18.setVisible(true);
                    }else{
                        jLabel18.setVisible(false);
                        
                    }
                    
                    if (jTextField7.getText().length()>6){
                        jLabel19.setVisible(true);
                    }else{
                        jLabel19.setVisible(false);
                        
                    }
                    if (jTextField8.getText().equals(jTextField7.getText())&&!jTextField8.getText().equals("")){
                        jLabel20.setVisible(true);
                    }else{
                        jLabel20.setVisible(false);
                        
                    }
                    
                    
                    if(jLabel18.isVisible()&&jLabel19.isVisible()&&jLabel20.isVisible()){
                        jButton3.setEnabled(true);
                    }else{
                        jButton3.setEnabled(false);
                    }
                    if((jTextField4.getText().length()>0&&jTextField5.getText().length()>0)
                            &&(!jTextField4.getText().equals(jTable1.getValueAt(jTable1.getSelectedRow(), 0))
                            ||(!jTextField5.getText().equals(jTable1.getValueAt(jTable1.getSelectedRow(), 1))))){
                        
                        jButton2.setEnabled(true);
                    }else{
                        jButton2.setEnabled(false);
                    }
                } catch (Exception e) {
                }
            }
            
            
            
            
        };
        
        new Timer(delay, taskPerformer).start();
    }
