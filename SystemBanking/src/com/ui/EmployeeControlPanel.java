    public void updateEmployeeList(){
        
        for(int i=0;i< EmployeeList.toArray().length;i++){
            if (EmployeeList.get(i).UserName.equals(jTable1.getValueAt(jTable1.getSelectedRow(), 0))){
                if (UI.username.equals(EmployeeList.get(i).UserName)){
                    UI.username=jTextField4.getText();
                    
                    UI.password=jTextField5.getText();
                    UI.UserEmployee=EmployeeList.get(i);
                    UI.EnableProfileButton();
                }
                
                jTable1.setValueAt(jTextField4.getText(),jTable1.getSelectedRow(), 0);
                jTable1.setValueAt(jTextField5.getText(),jTable1.getSelectedRow(), 1);
                
                EmployeeList.get(i).UserName=jTextField4.getText();
                EmployeeList.get(i).Password=jTextField5.getText();
                EmployeeList.get(i).Update=jCheckBox13.isSelected();
                EmployeeList.get(i).Delete=jCheckBox9.isSelected();
                EmployeeList.get(i).CanCreate=jCheckBox15.isSelected();
                EmployeeList.get(i).CONTROL_UPDATE=jCheckBox16.isSelected();
                EmployeeList.get(i).Employee_Chat=jCheckBox18.isSelected();
                EmployeeList.get(i).CONTROL_CREATE=jCheckBox14.isSelected();
                EmployeeList.get(i).admin=jCheckBox17.isSelected();
                
                //  Employee.SaveEmployees(EmployeeList);
                SystemBanking.EmployeeClient.send(
                        new Message("UpdateOwenEmployeeList",EmployeeList.get(i).UserName, EmployeeList, "SERVER"));
                break;
            }
        }
        
    }
