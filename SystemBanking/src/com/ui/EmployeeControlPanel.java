    public void UpdateEmployeeList(ArrayList<Employee> EMPLIST){
        try {
            
            
            ClearTableEmployees();
            this.EmployeeList=EMPLIST;
            //  updateEmployeeList();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            for(Employee EMP :EMPLIST){
                
                Object[] row = new Object[3];
                row[0]=EMP.UserName;
                row[1]=EMP.Password;
                row[2]=GetRWS(EMP);
                model.addRow(row);
            }
            
            
            jTable1.setModel(model);
        } catch (Exception e) {
        }
    }
    public void JtableSelectionChanged() {
        try {
            
            
            jTextField4.setText((String)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            jTextField5.setText((String)jTable1.getValueAt(jTable1.getSelectedRow(), 1));
            Employee CurrEmp=null;
            for(Employee EMP :EmployeeList){
                if (EMP.UserName.equals(jTextField4.getText()))
                    CurrEmp=EMP;
            }
            jCheckBox13.setSelected(CurrEmp.Update);
            jCheckBox15.setSelected(CurrEmp.CanCreate);
            jCheckBox9.setSelected(CurrEmp.Delete);
            jCheckBox17.setSelected(CurrEmp.admin);
            jCheckBox14.setSelected(CurrEmp.CONTROL_CREATE);
            if (CurrEmp.admin){
                
                jCheckBox14.setEnabled(false);
                jCheckBox17.setEnabled(false);
                jCheckBox16.setEnabled(false);
                jCheckBox13.setEnabled(false);
                jCheckBox18.setEnabled(false);
                jCheckBox9.setEnabled(false);
                jCheckBox15.setEnabled(false);
                jCheckBox17.setEnabled(false);
                jTextField4.setEnabled(false);
                jTextField5.setEnabled(false);
            }else{
                jCheckBox14.setEnabled(false);
                jCheckBox17.setEnabled(false);
                jCheckBox16.setEnabled(true);
                jCheckBox13.setEnabled(true);
                jCheckBox18.setEnabled(true);
                jCheckBox9.setEnabled(true);
                jCheckBox15.setEnabled(true);
                jCheckBox17.setEnabled(true);
                jTextField4.setEnabled(true);
                
            }
            if(CurrEmp.CONTROL_CREATE&&!CurrEmp.admin){
                
                jCheckBox14.setEnabled(true);
            }else{
                jCheckBox14.setEnabled(false);
            }
            if(SystemBanking.UserEmployee.admin&&!CurrEmp.admin){
                jCheckBox14.setEnabled(true);
                // jCheckBox17.setEnabled(true);
                jTextField5.setEnabled(true);
            }else {
                jCheckBox17.setEnabled(true);
                jTextField5.setEnabled(true);
            }
            jCheckBox16.setSelected(CurrEmp.CONTROL_UPDATE);
            jCheckBox18.setSelected(CurrEmp.Employee_Chat);
        } catch (Exception e) {
        }
    }
    public void ClearTableEmployees()
    {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int rows = model.getRowCount();
            for (int i = rows - 1; i >= 0; i--) {
                model.removeRow(i);
            }
        } catch (Exception e) {
            
        }
        
        
    }
    public String GetRWS(Employee EMP){
        String ResultString="";
        if (EMP.Update)
            ResultString+="Update,";
        //Delete
        if(EMP.Delete)
            ResultString+="Delete,";
        if(EMP.CanCreate)
            ResultString+="Create,";
        if(EMP.CONTROL_CREATE)
            ResultString+="CONTROL_CREATE,";
        if(EMP.CONTROL_UPDATE)
            ResultString+="CONTROL_UPDATE,";
        if(EMP.Employee_Chat)
            ResultString+="Employee_Chat,";
        if(EMP.admin)
            ResultString+=" admin";
        
        return ResultString;
    }
