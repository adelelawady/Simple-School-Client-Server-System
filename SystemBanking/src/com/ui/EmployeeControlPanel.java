    public void DeleteSelectedClient(){
        
        for(int i=0;i< EmployeeList.toArray().length;i++){
            if (EmployeeList.get(i).UserName.equals(jTable1.getValueAt(jTable1.getSelectedRow(), 0))){
                EmployeeList.remove(i);
                jTable1.remove(jTable1.getSelectedRow());
                //    Employee.SaveEmployees(EmployeeList);
                SystemBanking.EmployeeClient.send(
                        new Message("UpdateOwenEmployeeList",EmployeeList.get(i).UserName, EmployeeList, "SERVER"));
                break;
            }
        }
        
    }
